#[test]
fn shifting_rows() {
    let mut s: state_t = [[0; NB]; NB];
    #[allow(unused_assignments)]
    let mut t: state_t = [[0; NB]; NB];
    let mut a: state_t = [[0; NB]; NB];
    let mut c = 0;

    for i in 0..NB {
        for j in 0..NB {
            s[j][i] = c;
            c += 1;
        }
    }

    a[0][0] = 0;
    a[1][0] = 1;
    a[2][0] = 2;
    a[3][0] = 3;
    a[0][1] = 5;
    a[1][1] = 6;
    a[2][1] = 7;
    a[3][1] = 4;
    a[0][2] = 10;
    a[1][2] = 11;
    a[2][2] = 8;
    a[3][2] = 9;
    a[0][3] = 15;
    a[1][3] = 12;
    a[2][3] = 13;
    a[3][3] = 14;

    t = shift_rows(&s);

    for i in 0..NB {
        for j in 0..NB {
            assert_eq!(a[j][i], t[j][i]);
        }
    }
}

#[test]
fn cipher() {
    let key: [u8; 16] = [
        0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f,
        0x3c,
    ];
    let out: [u8; 16] = [
        0x3a, 0xd7, 0x7b, 0xb4, 0x0d, 0x7a, 0x36, 0x60, 0xa8, 0x9e, 0xca, 0xf3, 0x24, 0x66, 0xef,
        0x97,
    ];
    let input: [u8; 16] = [
        0x6b, 0xc1, 0xbe, 0xe2, 0x2e, 0x40, 0x9f, 0x96, 0xe9, 0x3d, 0x7e, 0x11, 0x73, 0x93, 0x17,
        0x2a,
    ];

    let ctx = aes_init_ctx(key);

    let output = aes_ecb_encrypt(&ctx, &input);

    for i in 0..out.len() {
        assert_eq!(out[i], output[i]);
    }
}
